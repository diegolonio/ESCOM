// -------------------- Configuración I2C --------------------
void i2c_init(void) {
    PORTC |= (1 << PC4) | (1 << PC5); 
    TWSR = 0x00;
    TWBR = 12; // 400kHz
    TWCR = (1 << TWEN);
}

void i2c_start(void) {
    TWCR = (1 << TWINT) | (1 << TWSTA) | (1 << TWEN);
    while (!(TWCR & (1 << TWINT)));
}

void i2c_stop(void) {
    TWCR = (1 << TWINT) | (1 << TWSTO) | (1 << TWEN);
}

void i2c_write(uint8_t data) {
    TWDR = data;
    TWCR = (1 << TWINT) | (1 << TWEN);
    while (!(TWCR & (1 << TWINT)));
}

void dac_write(uint16_t value) {
    uint8_t high = (uint8_t)((value >> 8) & 0x0F);
    uint8_t low  = (uint8_t)(value & 0xFF);
    i2c_start();
    i2c_write(MCP4725_ADDR << 1);
    i2c_write(high);
    i2c_write(low);
    i2c_stop();
}

// -------------------- Configuración Timer1 --------------------
void timer1_init(void) {
    // Modo CTC, Prescaler de 8: 16MHz / 8 = 2MHz (Cada tick es 0.5 us)
    TCCR1B = (1 << WGM12) | (1 << CS11);
    
    // Queremos actualizar la señal cada 200 us: 200 / 0.5 = 400 ticks (-1 porque empieza en 0)
    OCR1A = 399;
}

void wait_for_timer(void) {
    while (!(TIFR1 & (1 << OCF1A))); // Congela el programa hasta el microsegundo exacto
    TIFR1 = (1 << OCF1A);            // Limpia la bandera para el siguiente ciclo
}

// 
// ── Lógica Principal ─────────────────────────────────────────
int main(void) {
    i2c_init();
    timer1_init();
    
    DDRB |= (1 << PB5); // LED Pin 13
    
    TipoOnda modoActual = SENOIDAL;
    uint16_t idx = 0;
    uint16_t dac_val = 0;

    srand(TCNT1);
    uint16_t caos_restante = 0;  // puntos de perturbación que quedan
    uint16_t enfria_restante = 0;  // calma forzada tras la perturbación
    int16_t caos_offset = 0;  // desviación aplicada (cuentas DAC)

    while (1) {
        // 1. Leer de la memoria permanente dependiendo del modo
        switch (modoActual) {
            case SENOIDAL:
                dac_val = pgm_read_word(&TABLA_SENOIDAL[idx]);
                break;
            case CUADRADA:
                dac_val = pgm_read_word(&TABLA_CUADRADA[idx]);
                break;
            case SIERRA:
                dac_val = pgm_read_word(&TABLA_SIERRA[idx]);
                break;
            case TRIANGULAR:
                dac_val = pgm_read_word(&TABLA_TRIANGULAR[idx]);
                break;
        }

        // 2. Insertar perturbaciones en la señal si el modo caos está activo
        if (caos_restante > 0) {
            int32_t v = (int32_t)dac_val + caos_offset;
            if (v < 0)    v = 0;
            if (v > 4095) v = 4095;
            dac_val = (uint16_t)v;
            caos_restante--;
            if (caos_restante == 0) enfria_restante = 600; // dejar respirar
        } else if (enfria_restante > 0) {
            enfria_restante--; // señal limpia
        } else if (CAOS == true && (rand() % 2200) == 0) {
            // Nuevo disparo aleatorio
            if (rand() & 1) caos_restante = 25;   // corta -> AMARILLO
            else            caos_restante = 100;  // larga -> AZUL
            caos_offset = (dac_val < 2048) ? 1200 : -1200; // desviación grande
        }

        // 3. Escribir al DAC
        dac_write(dac_val);

        // 4. Avanzar el índice de la onda
        idx++;
        if (idx >= PUNTOS) {
            idx = 0;
            PORTB ^= (1 << PB5); // Parpadea el LED del UNO al completar 1 ciclo
        }

        // 5. Esperar al Timer1
        wait_for_timer(); 
    }
    
    return 0;
}
